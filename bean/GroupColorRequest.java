package com.fousalert.bean;

public class GroupColorRequest {
	private String chartUID;
	private String groupColor;
	private String apiSerial;
	
	public String getApiSerial() {
		return apiSerial;
	}
	public void setApiSerial(String apiSerial) {
		this.apiSerial = apiSerial;
	}
	public String getChartUID() {
		return chartUID;
	}
	public void setChartUID(String chartUID) {
		this.chartUID = chartUID;
	}
	public String getGroupColor() {
		return groupColor;
	}
	public void setGroupColor(String groupColor) {
		this.groupColor = groupColor;
	}
	
	
}
